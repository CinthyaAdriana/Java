
public class VariablesTiposDeDatos { 
	public static void main(String[] args) {
		
		//String permite almacenar cadenas de texto 
		String nombre = "Cinthya Adriana";
		//int edad = 27; 
		
		//char nos permite almacenar cadenas de texto 
		char inicial = 'C';  
		
		// boolean almacena un valor booleano, puede ser true o false 
		boolean tienesCrush = false; 
		
		// Tipos de Datos Enteros 
		
		//byte 
		byte edad = 27;
		
		// short 
		short suma = 24 + 104; 
		
		//int 
		int numero = 12036974; 
		
		//long 
		long numeroLargo = 100_000_000; 
		
		System.out.println(numeroLargo); 
		
		//float 
		float promedio = 1232.23232f; /* f para especificar que es un float y no un double */
		
		//double 
		double numeroPi = 3.14159;
		
		
		/*
		Javascript
		let numero = 10; 
		let nombre = "Cinthya Adriana"
		*/
	}

}

